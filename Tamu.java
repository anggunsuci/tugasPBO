public class Tamu {
    private String nama;
    private String nomorTelepon;

    public Tamu(String nama, String nomorTelepon) {
        this.nama = nama;
        this.nomorTelepon = nomorTelepon;
    }

    public String getNama() { return nama; }
    public String getNomorTelepon() { return nomorTelepon; }
}
